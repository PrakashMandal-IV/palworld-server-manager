const FALLBACK = ["#####", "#...#", "#...#", "#...#", "#...#", "#...#", "#####"];

// Compile each glyph to 7 row masks (bit GLYPH_W-1 = leftmost column).
function compile(rows) {
  return rows.map((r) => {
    let m = 0;
    for (let c = 0; c < GLYPH_W; c++) if (r[c] === "#") m |= 1 << (GLYPH_W - 1 - c);
    return m;
  });
}
const GLYPHS = {};
for (const [ch, rows] of Object.entries(ART)) GLYPHS[ch] = compile(rows);
const FALLBACK_MASK = compile(FALLBACK);

function rowsFor(ch) { return GLYPHS[ch] || FALLBACK_MASK; }

// Pixel width of a string at the given integer scale (1px inter-glyph gap).
function textWidth(text, scale) {
  const n = [...String(text)].length;
  if (!n) return 0;
  return (n * GLYPH_W + (n - 1)) * scale;
}

module.exports = { GLYPH_W, GLYPH_H, rowsFor, textWidth };
